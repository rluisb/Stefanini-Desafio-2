
public class Navegador {
	
	private String nome;
	private int acessos;

	public Navegador(String nome, int acessos) {
		this.nome = nome;
		this.acessos = acessos;
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public int getAcessos() {
		return acessos;
	}

	public void setAcessos(int acessos) {
		this.acessos = acessos;
	}
	
	
	
	
	
	
	

}
